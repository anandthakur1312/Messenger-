package com.anand.messenger.model;

public class Link {
	private String uri;
	private String rel;
	
	public String getLink() {
		return uri;
	}
	public void setLink(String link) {
		this.uri = link;
	}
	public String getRel() {
		return rel;
	}
	public void setRel(String rel) {
		this.rel = rel;
	}
	
	
}
